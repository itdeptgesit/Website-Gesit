const { createClient } = require('@supabase/supabase-js');
require('dotenv').config({ path: '.env.local' });

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl || !supabaseServiceKey) {
    console.error("Missing Supabase credentials in .env.local");
    process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseServiceKey);

async function setup() {
    console.log("Setting up page_analytics table...");

    // Since we can't run raw SQL easily via the JS client without a stored procedure,
    // we'll try to insert a dummy row to see if the table exists, 
    // but the best way is to tell the user to run SQL or use a migration if available.
    // However, I will try to use the 'rpc' method if 'admin' extensions are enabled, 
    // or just assume the user will let me try to create it via a hack or just provide the SQL.
    
    // Actually, I'll provide the SQL and a simple instructions, 
    // but I can also try to "upsert" which will fail if table doesn't exist.
    
    console.log("Please run the following SQL in your Supabase SQL Editor:");
    console.log(`
CREATE TABLE IF NOT EXISTS public.page_analytics (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    path TEXT UNIQUE NOT NULL,
    name TEXT NOT NULL,
    views BIGINT DEFAULT 0,
    last_visited TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Initial data
INSERT INTO public.page_analytics (path, name, views)
VALUES 
('/', 'Home Page', 0),
('/csr', 'CSR Page', 0),
('/about-us', 'About Us', 0),
('/news', 'News Archive', 0),
('/our-business', 'Business Overview', 0)
ON CONFLICT (path) DO NOTHING;
    `);
}

setup();
